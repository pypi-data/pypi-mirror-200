def check_and_create_path(full_file_name: str) -> bool:
    # create folder if not exist
    file_path, file_name = os.path.split(full_file_name)
    if not os.path.isdir(file_path):
        try:
            os.makedirs(file_path, exist_ok=True)
        except OSError:
            return False
    return True
